# Programa que leia dois números inseridos pelo utilizador e apresentar sua soma, subtração, divisão e resto.

num1 = int(input('Digite um número - '))
num2 = int(input('Digite outro número - '))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2
resto = num1 % num2

print(soma)
print(subtracao)
print(multiplicacao)
print(divisao)
print(resto)
